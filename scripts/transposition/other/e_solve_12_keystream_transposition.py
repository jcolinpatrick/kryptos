def columnar_transposition_map(width, length, col_order):
    """
    Compute the output-to-input mapping for columnar transposition.

    Write input row-by-row into grid of given width.
    Read columns in col_order.

    Returns: output_to_input[out_pos] = in_pos
    That is, output[out_pos] = input[in_pos].
    """
    nrows = math.ceil(length / width)
    full_cols = length - (nrows - 1) * width  # number of columns with nrows elements
    # If length is exactly divisible, all columns are full
    if length % width == 0:
        full_cols = width

    # Number of elements in each column (by original column index)
    col_lengths = []
    for c in range(width):
        if c < full_cols:
            col_lengths.append(nrows)
        else:
            col_lengths.append(nrows - 1)

    # Build output-to-input mapping
    # Read columns in col_order
    output_to_input = [0] * length
    out_idx = 0
    for col_rank in range(width):
        col = col_order[col_rank]  # which original column to read
        for row in range(col_lengths[col]):
            in_pos = row * width + col
            if in_pos < length:
                output_to_input[out_idx] = in_pos
                out_idx += 1

    return output_to_input


def invert_map(mapping):
    """Invert a permutation mapping: input_to_output from output_to_input."""
    inv = [0] * len(mapping)
    for out_pos, in_pos in enumerate(mapping):
        inv[in_pos] = out_pos
    return inv


def check_consistency(source_positions, crib_keystream, period):
    """
    Check if the keystream values at crib positions are consistent with
    a periodic key of given period, after the source positions have been
    determined by inverting the columnar transposition.

    source_positions: dict {crib_pos: source_pos_in_pretransposition_keystream}
    crib_keystream: dict {crib_pos: keystream_value}
    period: period of the underlying key

    Returns: (consistent, residue_assignments)
    where residue_assignments maps residue_class -> key_value (or None if inconsistent)
    """
    residue_assignments = {}  # residue -> assigned key value

    for crib_pos in CRIB_POSITIONS_SORTED:
        source = source_positions[crib_pos]
        residue = source % period
        kval = crib_keystream[crib_pos]

        if residue in residue_assignments:
            if residue_assignments[residue] != kval:
                return False, None
        else:
            residue_assignments[residue] = kval

    return True, residue_assignments


def reconstruct_keystream(residue_assignments, period, width, col_order, length=CT_LEN):
    """
    Given residue assignments (residue -> key_value), period, and columnar transposition
    parameters, reconstruct the full 97-character keystream.

    Steps:
    1. Build the pre-transposition periodic keystream
    2. Apply the columnar transposition to get the actual keystream

    For residues not in residue_assignments, we try all 26 values (but for
    decryption we only need to check if the result is good).
    """
    # Build output_to_input map (output[out_pos] = input[in_pos])
    output_to_input = columnar_transposition_map(width, length, col_order)

    # Pre-transposition keystream: periodic with given period
    # Some residues may not be assigned (no crib constraint).
    # For those, we'd need to brute-force, but let's first try with
    # all unassigned residues set to 0 (just to score the crib positions).
    pre_trans = [0] * length
    assigned_residues = set(residue_assignments.keys())
    unassigned = [r for r in range(period) if r not in assigned_residues]

    for pos in range(length):
        residue = pos % period
        if residue in residue_assignments:
            pre_trans[pos] = residue_assignments[residue]
        else:
            pre_trans[pos] = 0  # placeholder

    # Apply columnar transposition: output[out_pos] = pre_trans[in_pos]
    keystream = [0] * length
    for out_pos in range(length):
        in_pos = output_to_input[out_pos]
        keystream[out_pos] = pre_trans[in_pos]

    return keystream, unassigned


def decrypt_vig(keystream):
    """Decrypt CT using Vigenere: PT[i] = (CT[i] - K[i]) mod 26"""
    return "".join(ALPH[(CT_VALS[i] - keystream[i]) % MOD] for i in range(CT_LEN))


def decrypt_beau(keystream):
    """Decrypt CT using Beaufort: PT[i] = (K[i] - CT[i]) mod 26"""
    return "".join(ALPH[(keystream[i] - CT_VALS[i]) % MOD] for i in range(CT_LEN))


def decrypt_var_beau(keystream):
    """Decrypt CT using Variant Beaufort: PT[i] = (CT[i] + K[i]) mod 26"""
    return "".join(ALPH[(CT_VALS[i] + keystream[i]) % MOD] for i in range(CT_LEN))


def brute_unassigned(keystream_template, unassigned_residues, period, width, col_order,
                     crib_keystream, variant_name, decrypt_fn):
    """
    For configurations with unassigned residues, try all 26^|unassigned| combinations.
    Only feasible for small numbers of unassigned residues (<=3).
    Returns best (score, plaintext, keystream, config_detail) or None.
    """
    n_unassigned = len(unassigned_residues)
    if n_unassigned > 3:
        # Too many to brute force; just score with placeholder
        return None

    output_to_input = columnar_transposition_map(width, CT_LEN, col_order)

    best_score = 0
    best_result = None

    # Try all combinations for unassigned residues
    for combo in range(26 ** n_unassigned):
        vals = []
        tmp = combo
        for _ in range(n_unassigned):
            vals.append(tmp % 26)
            tmp //= 26

        # Build pre-transposition keystream
        extra_assignments = dict(zip(unassigned_residues, vals))
        pre_trans = list(keystream_template)  # already has assigned residues

        for pos in range(CT_LEN):
            residue = pos % period
            if residue in extra_assignments:
                pre_trans[pos] = extra_assignments[residue]

        # Apply columnar transposition
        keystream = [0] * CT_LEN
        for out_pos in range(CT_LEN):
            keystream[out_pos] = pre_trans[output_to_input[out_pos]]

        # Decrypt and score
        pt = decrypt_fn(keystream)
        sc = sum(1 for pos, ch in CRIB_DICT.items() if pos < len(pt) and pt[pos] == ch)

        if sc > best_score:
            best_score = sc
            bean_result = verify_bean(keystream)
            best_result = (sc, pt, keystream, bean_result, extra_assignments)

    return best_result


