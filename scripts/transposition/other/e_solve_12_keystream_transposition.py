def get_col_element_positions(width, length):
    """For each original column c, return the set of input positions in that column."""
    nrows = math.ceil(length / width)
    full_cols = length - (nrows - 1) * width
    if length % width == 0:
        full_cols = width

    col_positions = {}
    col_len = {}
    for c in range(width):
        n = nrows if c < full_cols else nrows - 1
        col_len[c] = n
        col_positions[c] = [row * width + c for row in range(n) if row * width + c < length]
    return col_positions, col_len


def get_output_offset_for_col(col_rank, col_order, col_lengths):
    """Compute the starting output index for the col_rank-th column read."""
    offset = 0
    for r in range(col_rank):
        offset += col_lengths[col_order[r]]
    return offset


def search_with_constraint_prop(width, period, crib_keystream, decrypt_fn, variant_name):
    """
    Search column orderings using constraint propagation for large widths.

    For each partial column ordering, check if the crib positions that would
    be placed so far are consistent. Prune early if not.
    """
    nrows = math.ceil(CT_LEN / width)
    full_cols = CT_LEN - (nrows - 1) * width
    if CT_LEN % width == 0:
        full_cols = width

    col_positions, col_len = get_col_element_positions(width, CT_LEN)

    # For each column c, find which crib positions' OUTPUT indices fall into the
    # range that column c would occupy IF placed at a given rank.
    # Actually, simpler: for a partial ordering, compute the output positions
    # assigned so far and check consistency.

    results = {
        "tested": 0,
        "consistent": 0,
        "best_score": 0,
        "best_detail": None,
        "above_noise": [],
        "bean_pass": 0,
    }

    crib_pos_set = set(CRIB_POSITIONS_SORTED)

    def backtrack(partial_order, used_cols, output_offset, residue_map):
        """
        partial_order: columns placed so far (list of col indices)
        used_cols: set of columns already placed
        output_offset: next output position
        residue_map: dict {residue -> key_value} from constraints seen so far
        """
        rank = len(partial_order)

        if rank == width:
            # Complete ordering — this passed all checks during construction
            results["tested"] += 1
            results["consistent"] += 1

            col_order = tuple(partial_order)
            output_to_input = columnar_transposition_map(width, CT_LEN, col_order)

            source_positions = {}
            for crib_pos in CRIB_POSITIONS_SORTED:
                source_positions[crib_pos] = output_to_input[crib_pos]

            # We already verified consistency during backtracking, but reconstruct anyway
            keystream, unassigned = reconstruct_keystream(
                residue_map, period, width, col_order, CT_LEN
            )

            if len(unassigned) == 0:
                pt = decrypt_fn(keystream)
                sc = sum(1 for pos, ch in CRIB_DICT.items()
                         if pos < len(pt) and pt[pos] == ch)
                bean_result = verify_bean(keystream)
                if bean_result.passed:
                    results["bean_pass"] += 1
                if sc > results["best_score"] or (sc == results["best_score"] and bean_result.passed):
                    results["best_score"] = sc
                    results["best_detail"] = {
                        "variant": variant_name,
                        "period": period,
                        "width": width,
                        "col_order": list(col_order),
                        "score": sc,
                        "bean_pass": bean_result.passed,
                        "plaintext": pt,
                        "residue_assignments": dict(residue_map),
                        "n_unassigned": 0,
                    }
                if sc > NOISE_FLOOR:
                    results["above_noise"].append({
                        "variant": variant_name,
                        "period": period,
                        "width": width,
                        "col_order": list(col_order),
                        "score": sc,
                        "bean_pass": bean_result.passed,
                        "plaintext": pt[:40] + "...",
                    })
            elif len(unassigned) <= 3:
                pre_trans_template = [0] * CT_LEN
                for pos in range(CT_LEN):
                    residue = pos % period
                    if residue in residue_map:
                        pre_trans_template[pos] = residue_map[residue]
                result = brute_unassigned(
                    pre_trans_template, unassigned, period, width, col_order,
                    crib_keystream, variant_name, decrypt_fn
                )
                if result is not None:
                    sc, pt, ks, bean_result, extra = result
                    if bean_result.passed:
                        results["bean_pass"] += 1
                    if sc > results["best_score"] or (sc == results["best_score"] and bean_result.passed):
                        results["best_score"] = sc
                        full_res = dict(residue_map)
                        full_res.update(extra)
                        results["best_detail"] = {
                            "variant": variant_name,
                            "period": period,
                            "width": width,
                            "col_order": list(col_order),
                            "score": sc,
                            "bean_pass": bean_result.passed,
                            "plaintext": pt,
                            "residue_assignments": full_res,
                            "n_unassigned": len(unassigned),
                        }
                    if sc > NOISE_FLOOR:
                        results["above_noise"].append({
                            "variant": variant_name,
                            "period": period,
                            "width": width,
                            "col_order": list(col_order),
                            "score": sc,
                            "bean_pass": bean_result.passed,
                            "plaintext": pt[:40] + "...",
                            "n_unassigned": len(unassigned),
                        })
            return

        # Try placing each unused column at the current rank
        for col in range(width):
            if col in used_cols:
                continue

            # Check constraints: what crib positions fall in the output range
            # for this column at this rank?
            col_length = col_len[col]
            new_output_start = output_offset
            new_output_end = output_offset + col_length

            # Check if any crib positions fall in [new_output_start, new_output_end)
            new_residue_map = dict(residue_map)
            ok = True

            for out_pos in range(new_output_start, new_output_end):
                if out_pos in crib_pos_set:
                    # This output position is a crib position
                    # The input position is: the (out_pos - new_output_start)-th
                    # element of column `col`
                    row_in_col = out_pos - new_output_start
                    in_pos = row_in_col * width + col
                    residue = in_pos % period
                    kval = crib_keystream[out_pos]

                    if residue in new_residue_map:
                        if new_residue_map[residue] != kval:
                            ok = False
                            break
                    else:
                        new_residue_map[residue] = kval

            if ok:
                partial_order.append(col)
                used_cols.add(col)
                backtrack(partial_order, used_cols, output_offset + col_length, new_residue_map)
                partial_order.pop()
                used_cols.discard(col)

    backtrack([], set(), 0, {})
    return results


